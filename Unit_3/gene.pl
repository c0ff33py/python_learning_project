\vspace{0.5cm}

\textbf{ဤစာချုပ်သည် အလုပ်ရှင်နှင့် ဝန်ထမ်းအကြား သဘောတူညီမှုတစ်ခုဖြစ်ပြီး အောက်ပါအချက်များကို သဘောတူညီပါသည်။}

\vspace{0.5cm}

\section*{၁. လုပ်ငန်းသက်တမ်း}
ဤစာချုပ်သည် [စာချုပ်စတင်ရက်စွဲ] မှစတင်၍ အနည်းဆုံး ၆ လအထိ အကျုံးဝင်ပါသည်။ စာချုပ်သက်တမ်းကို နှစ်ဦးနှစ်ဘက်သဘောတူညီမှုဖြင့် သက်တမ်းတိုးနိုင်ပါသည်။

\section*{၂. လစာနှင့် အလုပ်ချိန်}
\begin{itemize}
    \item \textbf{လစာနှုန်းထား:}
    \begin{itemize}
        \item ၆ လပိုင်းနှင့် ၇ လပိုင်းတွင် ဝန်ထမ်းတစ်ဦးလျှင် တစ်လလျှင် ၂၀၀,၀၀၀ ကျပ် ပေးချေမည်။
        \item ၈ လပိုင်း (လိမ္မော်သီးရာသီချိန်) တွင် ဝန်ထမ်းတစ်ဦးလျှင် တစ်လလျှင် ၃၀၀,၀၀၀ ကျပ် ပေးချေမည်။
        \item အလုပ်မရှိသည့်ရက်များတွင်လည်း လစာကို ပုံမှန်ပေးချေမည်။
    \end{itemize}
    \item \textbf{အလုပ်ချိန်:} အလုပ်ပေါ်မူတည်၍ လုပ်ကိုင်ရမည်ဖြစ်ပြီး၊ အလုပ်ရှင်မှ သတ်မှတ်ထားသော လုပ်ငန်းတာဝန်များကို ထမ်းဆောင်ရမည်။
\end{itemize}

\section*{၃. အကျိုးခံစားခွင့်များ}
\begin{itemize}
    \item \textbf{ကားမောင်းသင်တန်း:} ဝန်ထမ်းများအား အလုပ်မရှိသည့်အခါများတွင် ကားမောင်းနည်းသင်တန်းကို အခမဲ့ပေးမည်။
    \item \textbf{အင်တာနက်အသုံးပြုခွင့်:} ဝန်ထမ်းများအား လုပ်ငန်းခွင်အတွင်း အင်တာနက်အခမဲ့အသုံးပြုခွင့်ပေးမည်။
    \item \textbf{အစားအသောက်:} ဝန်ထမ်းများအား အလုပ်ရှင်မှ ထောက်ပံ့ပေးသော အစားအသောက်များကို အခမဲ့ရရှိမည်။
\end{itemize}

\section*{၄. စာချုပ်ရပ်ဆိုင်းမှု}
\begin{itemize}
    \item ဝန်ထမ်းသည် အနည်းဆုံး ၆ လအထိ လုပ်ကိုင်ရန် ကတိပြုပါသည်။
    \item စာချုပ်သက်တမ်းမပြည့်မီ ဖျက်သိမ်းလိုပါက နှစ်ဦးနှစ်ဘက်သဘောတူညီမှုဖြင့် ရက် ၃၀ ကြိုတင်အကြောင်းကြားပြီး ရပ်ဆိုင်းနိုင်သည်။
    \item အလုပ်ရှင်မှ စာချုပ်ဖျက်သိမ်းလိုပါက ဝန်ထမ်းအား ရက် ၃၀ ကြိုတင်အကြောင်းကြားရမည်။
\end{itemize}

\section*{၅. အခြားသဘောတူညီချက်များ}
\begin{itemize}
    \item ဝန်ထမ်းသည် အလုပ်ရှင်မှ သတ်မှတ်ထားသော လုပ်ငန်းဆိုင်ရာ စည်းမျဉ်းစည်းကမ်းများကို လိုက်နာရမည်။
    \item ဤစာချုပ်တွင် ဖော်ပြမထားသော အခြားအချက်များကို နှစ်ဦးနှစ်ဘက်သဘောတူညီမှုဖြင့် ထပ်မံဖြည့်စွမ်းနိုင်သည်။
\end{itemize}

